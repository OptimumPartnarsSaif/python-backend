// Prompt the user to enter their name and store it in a variable
let name = prompt("Please enter your name:");

// Prompt the user to enter their age and store it in a variable
// The + symbol before prompt converts the string input to a number
let age = +prompt("Please enter your age:");

// Display the name and age in the console using a template literal
console.log(`Hello, my name is ${name} and I am ${age} years old.`);